class Article():
    def __init__(self, title, content, time, link, pic_link, press_name, press_num, cd_id, c_id):
        self.title = title
        self.content = content
        self.time = time
        self.link = link
        self.pic_link = pic_link
        self.press_name = press_name
        self.press_num = press_num
        self.cd_id = cd_id
        self.c_id = c_id